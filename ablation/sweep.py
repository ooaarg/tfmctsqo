def _abbrev(k: str) -> str:
    return _PATH_ABBREV.get(k, k[:3])


def _combo_value_slug(path: tuple[str, ...], value) -> str:
    if path and path[-1] == "full_budget":
        if isinstance(value, bool):
            enabled = value
        else:
            enabled = str(value).strip().lower() in {"1", "true", "t", "yes", "y", "on"}
        return "full" if enabled else "flex"
    if path and path[-1] == "search_algorithm":
        return {
            "mcts": "mcts",
            "saio": "saio",
            "iterative_improvement": "ii",
            "ii": "ii",
        }.get(str(value), str(value))
    if path and path[-1] == "expand_strategy":
        return {
            "mixed_025": "m025",
            "mixed_050": "m050",
            "selectivity": "sel",
        }.get(str(value), str(value))
    return str(value)


def expand_gucs_grid(gucs: dict) -> tuple[list[dict], list[tuple[str, ...]]]:
    """Cartesian-product over list-valued leaves.  Scalar-only input → one no-op combo.
    Returns (combos, varying_paths)."""
    leaves: list[tuple[tuple[str, ...], list]] = []

    def walk(d: dict, prefix: tuple[str, ...]) -> None:
        for k, v in d.items():
            if isinstance(v, dict):
                walk(v, (*prefix, k))
            elif isinstance(v, list):
                leaves.append(((*prefix, k), list(v)))
            else:
                leaves.append(((*prefix, k), [v]))

    walk(gucs, ())
    if not leaves:
        return [{}], []

    import itertools

    varying = [p for p, vals in leaves if len(vals) > 1]
    combos: list[dict] = []
    for choice in itertools.product(*[v for _, v in leaves]):
        combo: dict = {}
        for (path, _), val in zip(leaves, choice, strict=False):
            cur = combo
            for k in path[:-1]:
                cur = cur.setdefault(k, {})
            cur[path[-1]] = val
        combos.append(combo)
    return combos, varying


def combo_id(combo: dict, varying: list[tuple[str, ...]]) -> str:
    """Slug like `d3_ec1.41_Lsb80` from the varying paths; empty when grid is degenerate."""
    if not varying:
        return ""
    parts = []
    for path in varying:
        cur: object = combo
        for k in path:
            if isinstance(cur, dict):
                cur = cur.get(k)
            else:
                cur = None
                break
        slug_key = "_".join(_abbrev(k) for k in path)
        parts.append(f"{slug_key}{_combo_value_slug(path, cur)}")
    return "_".join(parts)


def apply_gucs(cfg: ExperimentConfig, cli_gucs: dict) -> tuple[dict, dict]:
    """Merge config + CLI gucs for each MCTS config.  Precedence: shared <
    config-specific < CLI.  CLI may be flat (`{"depth":5}` applies to both)
    or nested (`{"luby":{"phases":12}}` applies to luby only).

    `single.start_budget` is auto-derived from luby_cap unless explicitly set
    anywhere — keeps total iter budget matched without forcing two knobs to
    move together.
    """
    flat_cli = {k: v for k, v in cli_gucs.items() if not isinstance(v, dict)}
    nested_cli = {k: v for k, v in cli_gucs.items() if isinstance(v, dict)}

    def merge(which: str) -> dict:
        out = dict(cfg.gucs["shared"])
        out.update(cfg.gucs.get(which, {}))
        out.update(flat_cli)
        out.update(nested_cli.get(which, {}))
        return out

    luby_g = merge("luby")
    single_g = merge("single")

    # Was single.start_budget explicitly set anywhere?
    explicit_single_budget = (
        "start_budget" in cfg.gucs.get("single", {})
        or "start_budget" in nested_cli.get("single", {})
        or "start_budget" in flat_cli  # flat applies to both
    )
    if not explicit_single_budget and "start_budget" in luby_g and "phases" in luby_g:
        single_g["start_budget"] = luby_cap(int(luby_g["start_budget"]), int(luby_g["phases"]))

    return luby_g, single_g


