def append_jsonl(path: Path, row: dict) -> None:
    # fsync after each row so the streamlit UI sees them as they're written.
    with path.open("a") as f:
        f.write(json.dumps(row, sort_keys=False) + "\n")
        f.flush()
        os.fsync(f.fileno())


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Run an mcts_extreme ablation sweep.")
    parser.add_argument(
        "-c", "--config", type=Path, required=True, help="Path to .toml experiment config."
    )
    parser.add_argument(
        "-n",
        "--name",
        type=str,
        default=None,
        help="Override experiment name (default: from toml).",
    )
    parser.add_argument(
        "--run-group",
        type=str,
        default=None,
        help="Override UI run group name (default: from toml).",
    )
    parser.add_argument("--seeds", type=int, default=None, help="Override n_seeds.")
    parser.add_argument(
        "--queries", type=str, default=None, help='Override queries, e.g. "27a 27b 33a".'
    )
    parser.add_argument(
        "--rewards",
        type=str,
        default=None,
        help='Override reward maps, e.g. "neg_log norm_neg_log".',
    )
    parser.add_argument(
        "--aggs", type=str, default=None, help='Override UCT aggregations, e.g. "best average".'
    )
    parser.add_argument(
        "--configs", type=str, default=None, help='Override configs to compare, e.g. "luby single".'
    )
    parser.add_argument(
        "--gucs", type=str, default="{}", help="JSON GUC overrides; flat or nested by config name."
    )
    parser.add_argument(
        "--mcts-cells",
        type=str,
        default=None,
        help="JSON list of exact MCTS cells. Overrides TOML [[mcts_cells]]; [] disables them.",
    )
    parser.add_argument(
        "--source",
        type=str,
        default=None,
        help="Query source name (job, jobcomplex) or a directory path.",
    )
    parser.add_argument(
        "--runs-root",
        type=Path,
        default=_REPO_ROOT / "runs",
        help="Where to create the run directory.",
    )
    parser.add_argument(
        "--docker-container",
        type=str,
        default=None,
        help="Run psql/restarts through this Docker container instead of local pg_ctl/psql.",
    )
    parser.add_argument(
        "--pguser",
        type=str,
        default=None,
        help="Database user for Docker psql/pg_isready (default: PGUSER or postgres).",
    )
    parser.add_argument(
        "--measurement",
        choices=("full_protocol", "cost_only"),
        default=None,
        help="full_protocol = EXPLAIN ANALYZE; cost_only = EXPLAIN without execution.",
    )
    parser.add_argument(
        "--dry-run", action="store_true", help="Print sweep plan + run dir path, do nothing else."
    )
    parser.add_argument(
        "-j",
        "--parallel",
        type=int,
        default=1,
        help="Max cells to run concurrently per (query, seed) batch. "
        "0 = run all cells in parallel.  1 = sequential (default).",
    )
    parser.add_argument(
        "--prewarm",
        action="store_true",
        help="Run one EXPLAIN-only DP plan after each cluster restart, before launching "
        "parallel cells.  Off by default: empirically the prewarm warms shared_buffers in "
        "a way that occasionally perturbs MCTS plan_cost under parallel load (~1 in 24 "
        "runs in our smoke tests), likely floating-point ordering in cost summation.  "
        "Without prewarm, parallel runs reproduce sequential plan_costs exactly.",
    )
    parser.add_argument(
        "--drop-os-cache",
        action="store_true",
        help="After each cluster restart and before launching the parallel cell batch, "
        "drop Linux OS caches. Non-root runs use `sudo -n ablation/drop_caches.sh`, "
        "so configure passwordless sudo for that helper.",
    )
    parser.add_argument(
        "--drop-cache-command",
        type=str,
        default=os.environ.get("DROP_CACHE_COMMAND"),
        help="Optional command used by --drop-os-cache instead of the default helper. "
        "Parsed with shlex, not through a shell.",
    )
    parser.add_argument(
        "--restart-pg-per-batch",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Restart PostgreSQL before every (query, seed) parallel batch. Enabled by default.",
    )
    parser.add_argument(
        "--clean-per-run",
        action="store_true",
        help="Clean before every individual cell measurement instead of once per "
        "(query, seed) batch. Forces workers=1. Uses --restart-pg-per-batch and "
        "--drop-os-cache settings to decide which cleanup actions to perform.",
    )
    parser.add_argument(
        "--record-env",
        action="store_true",
        help="Record local environment knobs (pgbin, pgdata, pglog, pguser, db, "
        "docker_container) in config.json. Off by default to avoid leaking local "
        "paths/usernames into published runs.",
    )
    args = parser.parse_args(argv)

    cfg = load_config(args.config)
    if args.name:
        cfg.name = args.name
    if args.run_group is not None:
        cfg.run_group = args.run_group
    if args.seeds is not None:
        cfg.n_seeds = args.seeds
    if args.queries:
        cfg.queries = args.queries.split()
    if args.rewards:
        cfg.reward_modes = args.rewards.split()
    if args.aggs:
        cfg.uct_aggregations = args.aggs.split()
    if args.configs:
        cfg.configs = args.configs.split()
    if args.measurement is not None:
        cfg.measurement = args.measurement
    if cfg.measurement == "cost_only":
        cfg.n_seeds = 1

    cli_gucs = json.loads(args.gucs)
    if args.mcts_cells is not None:
        cfg.mcts_cells = json.loads(args.mcts_cells)
    if args.source:
        cfg.sources = parse_sources(args.source)
    if args.docker_container is not None:
        cfg.docker_container = args.docker_container
    if args.pguser is not None:
        cfg.pguser = args.pguser

    for src in cfg.sources:
        d = resolve_source_dir(src)
        if not d.is_dir():
            print(f"source directory not found: {d} (source={src!r})", file=sys.stderr)
            return 2
    query_paths: dict[str, Path] = {}
    for q in cfg.queries:
        p = find_query_source(q, cfg.sources)
        if p is None:
            print(f"missing query {q!r} in any source ({', '.join(cfg.sources)})", file=sys.stderr)
            return 3
        query_paths[q] = p

    selected_cells = _selected_mcts_cells(cfg) if cfg.mcts_cells else []
    if selected_cells:
        combos = [cell[-1] for cell in selected_cells]
        varying = []
        combo_ids = [cell[3] for cell in selected_cells]
    else:
        combos, varying = expand_gucs_grid(cli_gucs)
        combo_ids = [combo_id(c, varying) for c in combos]

    # MCTS configs span reward × agg × combo; baselines emit one cell per query
    # with sentinel "n/a" reward/agg + empty combo so the row schema stays uniform.
    cells: list[tuple[str, str, str, str, dict]] = []
    for config in cfg.configs:
        if config in BASELINE_CONFIGS:
            cells.append((config, "n/a", "n/a", "", {}))
        elif selected_cells:
            cells.extend([cell for cell in selected_cells if cell[0] == config])
        else:
            for reward in cfg.reward_modes:
                for agg in cfg.uct_aggregations:
                    for combo, cid in zip(combos, combo_ids, strict=False):
                        cells.append((config, reward, agg, cid, combo))
    total_cells = len(cells) * len(cfg.queries)
    total_runs = total_cells * cfg.n_seeds

    if args.dry_run:
        print(f"Experiment:    {cfg.name}")
        print(f"Queries ({len(cfg.queries)}): {' '.join(cfg.queries)}")
        if varying:
            print(f"Grid:          {len(combos)} combos varying {['.'.join(p) for p in varying]}")
        if selected_cells:
            print(f"Selected MCTS: {len(selected_cells)} cells")
        print(
            f"Cells ({len(cells)}): "
            + ", ".join(lib.short_label(c, r, a, cid) for c, r, a, cid, _ in cells)
        )
        print(f"Seeds:         {cfg.n_seeds}")
        print(f"Total cells:   {total_cells}")
        print(f"Total runs:    {total_runs}")
        ts = dt.datetime.now().strftime("%Y-%m-%dT%H-%M-%S")
        print(f"Run dir (preview): {args.runs_root}/{ts}-{cfg.name}")
        return 0

    if not cfg.docker_container and not cfg.pgdata:
        print(
            "PGDATA is not set — point it at your Postgres data dir.\n"
            "  PGDATA=/path/to/data PGBIN=/path/to/bin uv run sweep.py ...\n"
            "or use --docker-container <name> for Docker-backed runs.\n"
            "(PGBIN is optional if pg_ctl is on PATH.)",
            file=sys.stderr,
        )
        return 4
    if not cfg.docker_container and not Path(cfg.pgdata).is_dir():
        print(f"PGDATA does not exist: {cfg.pgdata}", file=sys.stderr)
        return 4

    # Fail fast within the UI's 1.2s launch-error window: if PG isn't reachable,
    # auto-start it; if startup fails, surface the real reason instead of letting
    # restart_cluster blow up mid-sweep with a buried errors.log.
    if not ensure_pg_running(cfg):
        return 5

    args.runs_root.mkdir(parents=True, exist_ok=True)
    run_dir, _combos2, _varying2 = init_run_dir(cfg, cli_gucs, args.runs_root, record_env=args.record_env)
    print(f"Run dir: {run_dir}", file=sys.stderr)
    print(
        f"Sweep plan: {len(cfg.queries)} queries x {len(cells)} cells x "
        f"{cfg.n_seeds} seeds = {total_runs} runs "
        f"(grid: {len(combos)} combos, varying={['.'.join(p) for p in varying]})",
        file=sys.stderr,
    )

    (run_dir / "pid").write_text(str(os.getpid()) + "\n")

    # SIGTERM/SIGINT → KeyboardInterrupt so the except-block flips status to "stopped".
    def _on_signal(signum, frame):
        raise KeyboardInterrupt(f"signal {signum}")

    signal.signal(signal.SIGTERM, _on_signal)
    signal.signal(signal.SIGINT, _on_signal)

    results_path = run_dir / "results.jsonl"

    config_sqlfile: dict[tuple[str, str], Path] = {}
    for cid in combo_ids:
        config_sqlfile[("luby", cid)] = run_dir / _sql_basename("luby", cid)
        config_sqlfile[("single", cid)] = run_dir / _sql_basename("single", cid)
    for bname in BASELINE_CONFIGS:
        p = run_dir / f"{bname}.sql"
        if p.exists():
            config_sqlfile[(bname, "")] = p

    tmpdir = Path(tempfile.mkdtemp(prefix="mcts_sweep."))
    measure_paths: dict[str, Path] = {
        q: build_measure_sql(query_paths[q], q, tmpdir, cfg.measurement) for q in cfg.queries
    }
    prewarm_paths: dict[str, Path] = {
        q: build_prewarm_sql(query_paths[q], q, tmpdir) for q in cfg.queries
    }
    query_source_name: dict[str, str] = {}
    for q in cfg.queries:
        p = query_paths[q]
        matched = next(
            (s for s in cfg.sources if resolve_source_dir(s).resolve() == p.parent.resolve()),
            p.parent.name,
        )
        query_source_name[q] = matched

    # Pre-create every cell dir up front so concurrent workers don't race on mkdir.
    for config, reward, agg, cid, _ in cells:
        for q in cfg.queries:
            (run_dir / cell_dirname(config, reward, agg, cid) / q).mkdir(
                parents=True, exist_ok=True
            )

    # Parallelism cap.  0 (default) = one worker per cell.  Barrier-synchronized launch
    # is only safe when workers == len(cells); otherwise workers run FIFO and we skip it.
    max_workers = args.parallel if args.parallel > 0 else len(cells)
    max_workers = max(1, min(max_workers, len(cells)))
    if args.clean_per_run:
        max_workers = 1
    use_barrier = max_workers == len(cells) and len(cells) > 1
    prewarm_enabled = args.prewarm
    clean_scope = "run" if args.clean_per_run else "batch"

    print(
        f"Run mode: workers={max_workers}/{len(cells)} cells, "
        f"clean_scope={clean_scope}, "
        f"restart_pg={'on' if args.restart_pg_per_batch else 'off'}, "
        f"prewarm={'on' if prewarm_enabled else 'off'}, "
        f"drop_os_cache={'on' if args.drop_os_cache else 'off'}, "
        f"barrier={'on' if use_barrier else 'off'}, "
        f"backend={'docker:' + cfg.docker_container if cfg.docker_container else 'local'}",
        file=sys.stderr,
    )

    total_batches = len(cfg.queries) * cfg.n_seeds
    batch_idx = 0
    started = time.time()

    def _clean_environment(q: str | None = None) -> None:
        if args.restart_pg_per_batch:
            try:
                restart_cluster(cfg)
            except subprocess.CalledProcessError as e:
                print(f"cluster restart failed: {e}", file=sys.stderr)
                raise
        time.sleep(0.3)

        if args.drop_os_cache:
            try:
                drop_os_caches(args.drop_cache_command)
            except subprocess.CalledProcessError as e:
                stderr = (e.stderr or "").strip()
                print(
                    "OS cache drop failed. Configure passwordless sudo for "
                    f"{DEFAULT_DROP_CACHES} or pass --drop-cache-command. "
                    f"rc={e.returncode}"
                    + (f"\nstderr: {stderr}" if stderr else ""),
                    file=sys.stderr,
                )
                raise

        if prewarm_enabled and q is not None:
            run_prewarm(cfg, prewarm_paths[q])

    def _measure_cell(
        cell: tuple[str, str, str, str, dict],
        q: str,
        seed: int,
        barrier: threading.Barrier | None,
    ) -> dict:
        config, reward, agg, cid, combo = cell
        sqlfile = config_sqlfile.get((config, cid if config not in BASELINE_CONFIGS else ""))
        if sqlfile is None:
            raise ValueError(f"no SQL template for config {config!r} combo {cid!r}")
        if barrier is not None:
            barrier.wait()
        if args.clean_per_run:
            print(
                f"  clean+run {q} seed={seed} "
                f"{lib.short_label(config, reward, agg, cid)}",
                file=sys.stderr,
            )
            _clean_environment(q)
        out = run_psql(
            cfg,
            sqlfile,
            reward=reward,
            agg=agg,
            seed=seed,
            query_sql=measure_paths[q],
        )
        plan_path = run_dir / cell_dirname(config, reward, agg, cid) / q / f"seed-{seed}.plan"
        plan_path.write_text(out)
        parsed = lib.parse_explain(out)
        return {
            "ts": dt.datetime.now().isoformat(timespec="milliseconds") + "Z",
            "query": q,
            "source": query_source_name.get(q, ""),
            "config": config,
            "reward": reward,
            "agg": agg,
            "combo_id": cid,
            "gucs": combo,
            "measurement": cfg.measurement,
            "seed": seed,
            **parsed,
            "plan_path": str(plan_path.relative_to(run_dir)),
        }

    try:
        for q in cfg.queries:
            for seed in range(1, cfg.n_seeds + 1):
                batch_idx += 1
                elapsed = int(time.time() - started)
                print(
                    f"[batch {batch_idx}/{total_batches}  elapsed {elapsed}s] "
                    f"query={q} seed={seed} cells={len(cells)}",
                    file=sys.stderr,
                )

                if not args.clean_per_run:
                    _clean_environment(q)

                if args.clean_per_run:
                    for cell in cells:
                        append_jsonl(results_path, _measure_cell(cell, q, seed, None))
                    continue

                barrier = threading.Barrier(len(cells)) if use_barrier else None
                # Preserve deterministic write order by indexing results to their cell slot.
                rows: list[dict | None] = [None] * len(cells)
                with cf.ThreadPoolExecutor(max_workers=max_workers) as ex:
                    futures = {
                        ex.submit(_measure_cell, cell, q, seed, barrier): i
                        for i, cell in enumerate(cells)
                    }
                    for fut in cf.as_completed(futures):
                        rows[futures[fut]] = fut.result()
                for row in rows:
                    if row is not None:
                        append_jsonl(results_path, row)

        lib.write_status(run_dir, lib.STATUS_DONE)
        print("Done.", file=sys.stderr)
        return 0

    except KeyboardInterrupt:
        lib.write_status(run_dir, lib.STATUS_STOPPED)
        (run_dir / "errors.log").write_text("interrupted\n")
        print("interrupted", file=sys.stderr)
        return 130
    except Exception as e:
        lib.write_status(run_dir, lib.STATUS_FAILED)
        import traceback

        (run_dir / "errors.log").write_text(traceback.format_exc())
        print(f"FAILED: {e}", file=sys.stderr)
        return 1
    finally:
        shutil.rmtree(tmpdir, ignore_errors=True)
        # Remove pid file so the UI knows the process is gone.
        with contextlib.suppress(FileNotFoundError):
            (run_dir / "pid").unlink()


if __name__ == "__main__":
    sys.exit(main())
