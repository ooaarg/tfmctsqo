def geqo_seed_value(seed: int) -> str:
    """Map integer experiment seeds to PostgreSQL's geqo_seed range."""
    phi_conjugate = 0.6180339887498949
    value = (int(seed) * phi_conjugate) % 1.0
    if value <= 0.0:
        value = 1.0
    return f"{value:.17g}"


