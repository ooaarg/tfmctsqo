def _pg(cfg: ExperimentConfig, tool: str) -> str:
    """`<pgbin>/<tool>` when pgbin is set, otherwise rely on PATH."""
    return f"{cfg.pgbin}/{tool}" if cfg.pgbin else tool


def _docker_exec(cfg: ExperimentConfig, *args: str, input_text: str | None = None):
    cmd = ["docker", "exec"]
    if input_text is not None:
        cmd.append("-i")
    cmd.append(cfg.docker_container)
    cmd.extend(args)
    return subprocess.run(cmd, input=input_text, capture_output=True, text=True, check=False)


def restart_cluster(cfg: ExperimentConfig) -> None:
    if cfg.docker_container:
        subprocess.run(
            ["docker", "restart", cfg.docker_container],
            check=True,
            stdout=subprocess.DEVNULL,
        )
        for _ in range(60):
            probe = _docker_exec(cfg, "pg_isready", "-U", cfg.pguser, "-d", cfg.db)
            if probe.returncode == 0:
                return
            time.sleep(1)
        raise subprocess.CalledProcessError(1, ["docker", "exec", cfg.docker_container, "pg_isready"])
    subprocess.run(
        [_pg(cfg, "pg_ctl"), "-D", cfg.pgdata, "-l", cfg.pglog, "restart", "-m", "fast", "-w"],
        check=True,
        stdout=subprocess.DEVNULL,
    )


def drop_os_caches(command: str | None = None) -> None:
    """Drop Linux page cache before a measurement batch.

    The default path uses a tiny root-owned operation.  When the sweep is not
    root, sudo is called with -n so Streamlit launches fail fast if the sudoers
    rule is missing instead of blocking on a password prompt.
    """
    if command:
        cmd = shlex.split(command)
    elif os.geteuid() == 0:
        cmd = [str(DEFAULT_DROP_CACHES)]
    else:
        cmd = ["sudo", "-n", str(DEFAULT_DROP_CACHES)]
    subprocess.run(cmd, check=True, stdout=subprocess.DEVNULL, stderr=subprocess.PIPE, text=True)


def ensure_pg_running(cfg: ExperimentConfig) -> bool:
    """Return True if PG is reachable (after starting it if needed); False otherwise.

    Prints actionable diagnostics to stderr on failure so the Streamlit UI's
    launch-log tail shows a real explanation instead of a buried mid-sweep crash.
    """
    if cfg.docker_container:
        probe = _docker_exec(cfg, "pg_isready", "-U", cfg.pguser, "-d", cfg.db)
        if probe.returncode == 0:
            return True
        print(
            f"Docker Postgres not reachable in {cfg.docker_container!r}: "
            f"{(probe.stdout or probe.stderr).strip()}",
            file=sys.stderr,
        )
        try:
            restart_cluster(cfg)
        except subprocess.CalledProcessError as e:
            print(f"docker restart failed for {cfg.docker_container!r}: {e}", file=sys.stderr)
            return False
        return True

    probe = subprocess.run(
        [_pg(cfg, "pg_isready"), "-h", "/tmp", "-d", cfg.db],
        capture_output=True,
        text=True,
        timeout=5,
    )
    if probe.returncode == 0:
        return True

    print(
        f"Postgres not reachable ({probe.stdout.strip() or 'pg_isready rc={probe.returncode}'}); "
        "attempting pg_ctl start...",
        file=sys.stderr,
    )
    try:
        subprocess.run(
            [_pg(cfg, "pg_ctl"), "-D", cfg.pgdata, "-l", cfg.pglog, "start", "-w"],
            check=True,
            capture_output=True,
            text=True,
            timeout=30,
        )
    except subprocess.CalledProcessError as e:
        print(
            f"pg_ctl start failed (rc={e.returncode}):\n"
            f"  stdout: {e.stdout.strip()}\n"
            f"  stderr: {e.stderr.strip()}\n"
            f"Tail of {cfg.pglog}:",
            file=sys.stderr,
        )
        with contextlib.suppress(OSError):
            print(Path(cfg.pglog).read_text()[-2000:], file=sys.stderr)
        print(
            f"\nFix manually then re-launch:\n"
            f"  {_pg(cfg, 'pg_ctl')} -D {cfg.pgdata} -l {cfg.pglog} start",
            file=sys.stderr,
        )
        return False
    except subprocess.TimeoutExpired:
        print(
            f"pg_ctl start timed out after 30s. Check {cfg.pglog} and any stale "
            f"postmaster.pid in {cfg.pgdata}.",
            file=sys.stderr,
        )
        return False
    return True


def build_measure_sql(raw_path: Path, query: str, dst: Path, measurement: str) -> Path:
    if not raw_path.exists():
        raise FileNotFoundError(f"missing query SQL: {raw_path}")
    suffix = "cost" if measurement == "cost_only" else "analyze"
    explain = (
        "EXPLAIN (COSTS on, SUMMARY on)\n"
        if measurement == "cost_only"
        else "EXPLAIN (ANALYZE, BUFFERS, COSTS on, SUMMARY on, TIMING on)\n"
    )
    out = dst / f"{query}.{suffix}.sql"
    out.write_text(explain + raw_path.read_text())
    return out


def build_prewarm_sql(raw_path: Path, query: str, dst: Path) -> Path:
    """EXPLAIN-only (no ANALYZE) variant used to warm pg_statistic + relcache after
    a cluster restart, so every parallel measurement cell sees identical warm state.
    Uses DP planner to keep the warmup deterministic and as cheap as possible."""
    out = dst / f"{query}.prewarm.sql"
    out.write_text(
        "LOAD 'mcts_extreme';\n"
        "SET pg_mcts_extreme.enabled = off;\n"
        "SET mcts_extreme.enabled = off;\n"
        "SET geqo = off;\n"
        "SET geqo_threshold = 64;\n"
        "EXPLAIN " + raw_path.read_text()
    )
    return out


def run_prewarm(cfg: ExperimentConfig, prewarm_sql: Path) -> None:
    """Single non-analyzing EXPLAIN to populate shared_buffers with catalog pages.
    Errors are swallowed — prewarm is best-effort, the measurement runs are what count."""
    if cfg.docker_container:
        _docker_exec(
            cfg,
            "psql",
            "-U",
            cfg.pguser,
            "-d",
            cfg.db,
            "-X",
            "-f",
            "-",
            input_text=prewarm_sql.read_text(),
        )
        return
    subprocess.run(
        [_pg(cfg, "psql"), "-h", "/tmp", "-d", cfg.db, "-X", "-f", str(prewarm_sql)],
        capture_output=True,
        check=False,
    )


def _inline_analyze_sql(sql_file: Path, query_sql: Path) -> str:
    return sql_file.read_text().replace(r"\i :analyze_sql", query_sql.read_text())


# Low-discrepancy Weyl sequence (golden-ratio fractional part).
# Maps integer seed N >= 1 to a distinct, well-spread float in (0, 1),
# suitable for SET geqo_seed which requires range [0.0, 1.0].
_GEQO_SEED_PHI = 0.6180339887498949  # (sqrt(5) - 1) / 2


def geqo_seed_value(seed_int: int) -> float:
    """Return a distinct float in (0, 1) for each positive integer seed."""
    return round((seed_int * _GEQO_SEED_PHI) % 1.0, 12) or 1e-6


def run_psql(
    cfg: ExperimentConfig, sql_file: Path, *, reward: str, agg: str, seed: int, query_sql: Path
) -> str:
    geqo_seed = geqo_seed_value(seed)
    common_vars = [
        "-v", f"reward={reward}",
        "-v", f"agg={agg}",
        "-v", f"seed={seed}",
        "-v", f"geqo_seed={geqo_seed}",
    ]
    if cfg.docker_container:
        result = _docker_exec(
            cfg,
            "psql",
            "-U",
            cfg.pguser,
            "-d",
            cfg.db,
            "-X",
            *common_vars,
            "-f",
            "-",
            input_text=_inline_analyze_sql(sql_file, query_sql),
        )
        return result.stdout + ("\n" + result.stderr if result.stderr else "")
    result = subprocess.run(
        [
            _pg(cfg, "psql"),
            "-h",
            "/tmp",
            "-d",
            cfg.db,
            "-X",
            *common_vars,
            "-v",
            f"geqo_seed={geqo_seed}",
            "-v",
            f"analyze_sql={query_sql}",
            "-f",
            str(sql_file),
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    return result.stdout + ("\n" + result.stderr if result.stderr else "")


def cell_dirname(config: str, reward: str, agg: str, cid: str = "") -> str:
    if config in BASELINE_CONFIGS:
        return config
    base = f"{config}__{reward}__{agg}"
    return f"{base}__{cid}" if cid else base


def _sql_basename(config: str, cid: str) -> str:
    return f"{config}__{cid}.sql" if cid else f"{config}.sql"


def _cell_combo(cell: dict) -> dict:
    combo: dict = {}
    for key in (
        "depth",
        "search_algorithm",
        "plan_shape",
        "force_left_tree",
        "top_k",
        "expand_strategy",
        "patience",
        "rollouts_per_leaf",
        "full_budget",
        "exploration_constant",
        "saio_max_iterations",
    ):
        if key in cell:
            combo[key] = cell[key]
    config = cell.get("config", "single")
    if "start_budget" in cell:
        combo.setdefault(config, {})["start_budget"] = cell["start_budget"]
    if "phases" in cell:
        combo.setdefault(config, {})["phases"] = cell["phases"]
    return combo


def _selected_mcts_cells(cfg: ExperimentConfig) -> list[tuple[str, str, str, str, dict]]:
    cells = []
    seen: set[tuple[str, str, str, str]] = set()
    for i, raw in enumerate(cfg.mcts_cells, start=1):
        config = str(raw.get("config", "single"))
        if config in BASELINE_CONFIGS:
            raise ValueError(f"mcts_cells[{i}] uses baseline config {config!r}")
        reward = str(raw["reward"])
        agg = str(raw.get("agg", "best"))
        combo = _cell_combo(raw)
        cid = str(raw.get("combo_id") or combo_id(combo, list(_flatten_paths(combo))))
        ident = (config, reward, agg, cid)
        if ident in seen:
            raise ValueError(f"duplicate mcts cell: {ident}")
        seen.add(ident)
        cells.append((config, reward, agg, cid, combo))
    return cells


def _flatten_paths(d: dict, prefix: tuple[str, ...] = ()):
    for key, value in d.items():
        if isinstance(value, dict):
            yield from _flatten_paths(value, (*prefix, key))
        else:
            yield (*prefix, key)


def init_run_dir(
    cfg: ExperimentConfig, cli_gucs: dict, runs_root: Path, record_env: bool = False
) -> tuple[Path, list[dict], list[tuple[str, ...]]]:
    ts = dt.datetime.now().strftime("%Y-%m-%dT%H-%M-%S")
    run_dir = runs_root / f"{ts}-{cfg.name}"
    run_dir.mkdir(parents=True, exist_ok=False)

    try:
        git_rev = (
            subprocess.run(
                ["git", "rev-parse", "HEAD"],
                capture_output=True,
                text=True,
                check=False,
            ).stdout.strip()
            or "(unknown)"
        )
    except Exception:
        git_rev = "(unknown)"

    selected_cells = _selected_mcts_cells(cfg) if cfg.mcts_cells else []
    if selected_cells:
        combos = [cell[-1] for cell in selected_cells]
        varying = []
    else:
        combos, varying = expand_gucs_grid(cli_gucs)

    md_grid_block: list[str] = []
    if selected_cells:
        md_grid_block.append("## Selected MCTS cells")
    else:
        md_grid_block.append("## Grid axes" if varying else "## GUCs (single point — no grid)")
    if varying:
        md_grid_block.append("```")
        for path in varying:
            vals = sorted({_get_path(c, path) for c in combos}, key=_sort_mixed_key)
            md_grid_block.append(f"{'.'.join(path)} ∈ {vals}")
        md_grid_block.append("```")
        md_grid_block.append(f"\n**{len(combos)} combos** total.\n")

    render_items = (
        [(config, cid, combo) for config, _reward, _agg, cid, combo in selected_cells]
        if selected_cells
        else [("luby", combo_id(combo, varying), combo) for combo in combos]
        + [("single", combo_id(combo, varying), combo) for combo in combos]
    )
    for config, cid, combo in render_items:
        luby_gucs, single_gucs = apply_gucs(cfg, combo)
        if config == "luby":
            gucs = luby_gucs
            sql = render_sql_template(repo_path(cfg.template_luby), gucs)
        elif config == "single":
            gucs = single_gucs
            sql = render_sql_template(repo_path(cfg.template_single), gucs)
        else:
            raise ValueError(f"unknown MCTS config: {config}")
        (run_dir / _sql_basename(config, cid)).write_text(sql)

        md_grid_block.append(f"### combo `{cid or '(single)'}`")
        md_grid_block.append("```")
        md_grid_block.append(f"{config}:")
        md_grid_block.extend(f"  {k} = {v}" for k, v in sorted(gucs.items()))
        md_grid_block.append("```")

    for bname, btemplate in BASELINE_CONFIGS.items():
        src = repo_path(btemplate)
        if src.exists():
            (run_dir / f"{bname}.sql").write_text(src.read_text())

    md = [
        f"# {cfg.name}",
        "",
        cfg.description or "",
        "",
        f"- **Started**: {ts}",
        f"- **Git revision**: `{git_rev}`",
        f"- **Run group**: {cfg.run_group or '(none)'}",
        f"- **n_seeds**: {cfg.n_seeds}",
        f"- **Configs**: {', '.join(cfg.configs)}",
        f"- **Reward modes**: {', '.join(cfg.reward_modes)}",
        f"- **UCT aggregations**: {', '.join(cfg.uct_aggregations)}",
        f"- **Queries** ({len(cfg.queries)}): {', '.join(cfg.queries)}",
        "",
        *md_grid_block,
    ]
    (run_dir / "README.md").write_text("\n".join(md) + "\n")

    # Save the resolved config (config + CLI overrides) for full reproducibility.
    config_dump = asdict(cfg)
    config_dump["cli_gucs"] = cli_gucs
    config_dump["grid_varying"] = [".".join(p) for p in varying]
    config_dump["n_combos"] = len(combos)
    if not record_env:
        # Local-environment knobs (paths, DB user, container) are omitted by
        # default so published runs don't leak them; pass --record-env to keep.
        for _k in ("pgbin", "pgdata", "pglog", "docker_container", "pguser", "db"):
            config_dump.pop(_k, None)
    (run_dir / "config.json").write_text(json.dumps(config_dump, indent=2, default=str))

    lib.write_status(run_dir, lib.STATUS_RUNNING)
    return run_dir, combos, varying


def _get_path(d: dict, path: tuple[str, ...]):
    cur: object = d
    for k in path:
        if isinstance(cur, dict):
            cur = cur.get(k)
        else:
            return None
    return cur


