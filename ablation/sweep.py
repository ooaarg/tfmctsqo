def resolve_source_dir(source: str) -> Path:
    """Turn a source name or path into a directory containing `<q>.sql` files."""
    return Path(KNOWN_SOURCES.get(source, source))


def parse_sources(raw) -> list[str]:
    """Accept str (single or comma-sep) or list[str]; return list."""
    if raw is None:
        return ["job"]
    if isinstance(raw, list):
        return [str(x) for x in raw if str(x).strip()]
    s = str(raw)
    if "," in s:
        return [t.strip() for t in s.split(",") if t.strip()]
    return [s.strip()]


def find_query_source(query: str, sources: list[str]) -> Path | None:
    """If query has a `source:name` prefix, use it.  Else search each source
    dir in declaration order and return the first hit.  None if not found.
    """
    if ":" in query:
        src, name = query.split(":", 1)
        d = resolve_source_dir(src)
        f = d / f"{name}.sql"
        return f if f.is_file() else None
    for src in sources:
        d = resolve_source_dir(src)
        f = d / f"{query}.sql"
        if f.is_file():
            return f
    return None


