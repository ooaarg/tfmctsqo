# --- GUC override + SQL templating ---

# Luby restart-sequence weights.  weights[k-1] = luby(k); the cumulative sum
# of these gives the total iter cap when start_budget is per-phase.
_LUBY_WEIGHTS = (1, 1, 2, 1, 1, 2, 4, 1, 1, 2, 1, 1, 2, 4, 8, 1)


def _sort_mixed_key(value):
    """Sort key for mixed int/str sequences: numerics first, natural order within group."""
    return (isinstance(value, str), value)


def luby_cap(start_budget: int, phases: int) -> int:
    """start_budget × sum_{k=1..phases} luby(k); capped at len(_LUBY_WEIGHTS)."""
    if phases <= 0:
        return 0
    phases = min(phases, len(_LUBY_WEIGHTS))
    return start_budget * sum(_LUBY_WEIGHTS[:phases])


_SET_LINE_RE_TPL = r"^(\s*SET\s+mcts_extreme\.{name}\s*=\s*)[^;]+;"


def _sub_guc(sql: str, name: str, value) -> str:
    if isinstance(value, bool):
        value = "on" if value else "off"
    elif isinstance(value, str) and not value.startswith("'"):
        value = "'" + value.replace("'", "''") + "'"
    pat = re.compile(_SET_LINE_RE_TPL.format(name=re.escape(name)), re.M)
    return pat.sub(rf"\g<1>{value};", sql)


def render_sql_template(template_path: Path, gucs: dict) -> str:
    """Read template SQL, apply every GUC override.

    ``gucs`` is a flat dict (already merged: shared <- config-specific
    <- CLI overrides).  Unknown keys are silently ignored — the template
    may not contain a SET line for every possible GUC.
    """
    sql = template_path.read_text()
    for name, value in gucs.items():
        sql = _sub_guc(sql, name, value)
    return sql


